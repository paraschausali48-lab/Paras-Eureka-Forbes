// Language switcher functionality
document.addEventListener('DOMContentLoaded', function() {
  let currentLanguage = 'en';
  
  // Get language buttons
  const langButtons = document.querySelectorAll('.lang-btn');
  
  // Initialize translations on page load
  updateAllTranslations('en');
  
  // Add click listeners to language buttons
  langButtons.forEach(btn => {
    btn.addEventListener('click', function() {
      const lang = this.getAttribute('data-lang');
      currentLanguage = lang;
      
      // Update active button
      langButtons.forEach(b => b.classList.remove('active'));
      this.classList.add('active');
      
      // Update all translations
      updateAllTranslations(lang);
      
      // Store preference
      localStorage.setItem('preferredLanguage', lang);
    });
  });
  
  // Load saved language preference
  const savedLang = localStorage.getItem('preferredLanguage') || 'en';
  if (savedLang !== 'en') {
    const savedBtn = document.querySelector(`[data-lang="${savedLang}"]`);
    if (savedBtn) {
      savedBtn.click();
    }
  }
  
  function updateAllTranslations(lang) {
    // Update all elements with data-i18n attribute
    const elements = document.querySelectorAll('[data-i18n]');
    elements.forEach(el => {
      const key = el.getAttribute('data-i18n');
      if (translations[lang][key]) {
        // For input placeholder, update the placeholder attribute
        if (el.tagName === 'INPUT' && el.type === 'text') {
          el.placeholder = translations[lang][key];
        } else {
          el.textContent = translations[lang][key];
        }
      }
    });
    
    // Update product descriptions
    const productCards = document.querySelectorAll('.product-card');
    productCards.forEach(card => {
      const h3 = card.querySelector('h3');
      const desc = card.querySelector('p');
      const tag = card.querySelector('.product-tag');
      const btn = card.querySelector('.product-btn');
      
      if (h3 && desc && translations[lang][h3.textContent]) {
        desc.textContent = translations[lang][h3.textContent];
      }
      
      if (tag) {
        const categoryKey = tag.dataset.category || tag.textContent;
        const key = 'tag_' + categoryKey.toLowerCase().replace(/\s+/g, '_');
        if (translations[lang][key]) {
          tag.textContent = translations[lang][key];
        }
      }
      
      if (btn) {
        btn.textContent = translations[lang]['btn_ask'];
      }
    });
  }
});
